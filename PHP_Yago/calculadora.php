<?php
$op1 = $_GET['op1'];
$op2 = $_GET['op2'];
$op = $GET['op'];


function sumar ($op1, $op2){
	return $op1 + $op2
}

function restar ($op1, $op2){
	return $op1 - $op2
}

function multiplicar ($op1, $op2){
	return $op1 * $op2
}

function dividir ($op1, $op2){
	return $op1 / $op2
}

switch($op) {
	case "sumar":
	break;
	
	case "restar":
	break;
	
	case "multiplicar":
	break;
	
	case "dividir":
	break;
	default:
}





?>